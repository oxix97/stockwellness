-- 주요 시장 지수(Benchmark) 시세 테이블 생성
DO $$
BEGIN
    IF NOT EXISTS (SELECT FROM pg_tables WHERE schemaname = 'public' AND tablename = 'benchmark_price') THEN
        CREATE TABLE benchmark_price (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            created_at TIMESTAMP(6) NOT NULL,
            updated_at TIMESTAMP(6),
            ticker VARCHAR(20) NOT NULL,
            base_date DATE NOT NULL,
            open_price NUMERIC(19, 4),
            high_price NUMERIC(19, 4),
            low_price NUMERIC(19, 4),
            close_price NUMERIC(19, 4) NOT NULL,
            change_rate NUMERIC(10, 4),
            volume BIGINT
        );
    END IF;
END $$;

-- 인덱스 생성 (인덱스는 IF NOT EXISTS 지원)
CREATE UNIQUE INDEX IF NOT EXISTS idx_benchmark_ticker_date ON benchmark_price (ticker, base_date);
CREATE INDEX IF NOT EXISTS idx_benchmark_date ON benchmark_price (base_date);
