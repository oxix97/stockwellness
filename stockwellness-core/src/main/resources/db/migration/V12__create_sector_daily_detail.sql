create table if not exists sector_daily_detail
(
    id                           bigint generated by default as identity primary key,
    created_at                   timestamp(6) not null,
    updated_at                   timestamp(6),
    index_code                   varchar(10)  not null,
    index_name                   varchar(100) not null,
    base_date                    date         not null,
    current_price                numeric(19, 4),
    change_amount                numeric(19, 4),
    change_sign                  varchar(4),
    change_rate                  numeric(10, 4),
    accumulated_volume           bigint,
    previous_volume              bigint,
    accumulated_trading_amount   bigint,
    previous_trading_amount      bigint,
    open_price                   numeric(19, 4),
    high_price                   numeric(19, 4),
    low_price                    numeric(19, 4),
    rising_issue_count           integer,
    upper_limit_issue_count      integer,
    steady_issue_count           integer,
    falling_issue_count          integer,
    lower_limit_issue_count      integer,
    yearly_high_price            numeric(19, 4),
    yearly_high_rate             numeric(10, 4),
    yearly_high_date             date,
    yearly_low_price             numeric(19, 4),
    yearly_low_rate              numeric(10, 4),
    yearly_low_date              date,
    total_ask_residual_quantity  bigint,
    total_bid_residual_quantity  bigint,
    sell_residual_rate           numeric(10, 4),
    buy_residual_rate            numeric(10, 4),
    net_buy_residual_quantity    bigint,
    net_foreign_buy_amount       bigint,
    net_inst_buy_amount          bigint,
    constraint uk_sector_daily_detail unique (index_code, base_date)
);

create index if not exists idx_sector_daily_detail_base_date
    on sector_daily_detail (base_date);
